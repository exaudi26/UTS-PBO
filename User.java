public class User {
    String nama;
    String alamat;
    String nomortelepon;
    double saldo;
    String tanggalregistrasi;
    String nomorakun;

    User(String nama, String alamat, String nomortelepon, double saldo, String tanggalregistrasi, String nomorakun) {
        this.nama = nama;
        this.alamat = alamat;
        this.nomortelepon = nomortelepon;
        this.saldo = saldo;
        this.tanggalregistrasi = tanggalregistrasi;
        this.nomorakun = nomorakun;
    }
}
